package com.ddcar.pojo;

import java.util.List;
import java.util.Map;

/**
 * 团签用户详情
 * @author wcy
 */
public class SignUserDetail extends SignUser {

    private Long parentId;
    private String idCartPhotoFront;
    private String idCartPhotoSide;
    private List<Map<String, Object>> leaseHistory;

    public Long getParentId() {
        return parentId;
    }

    public void setParentId(Long parentId) {
        this.parentId = parentId;
    }

    public String getIdCartPhotoFront() {
        return idCartPhotoFront;
    }

    public void setIdCartPhotoFront(String idCartPhotoFront) {
        this.idCartPhotoFront = idCartPhotoFront;
    }

    public String getIdCartPhotoSide() {
        return idCartPhotoSide;
    }

    public void setIdCartPhotoSide(String idCartPhotoSide) {
        this.idCartPhotoSide = idCartPhotoSide;
    }

    public List<Map<String, Object>> getLeaseHistory() {
        return leaseHistory;
    }

    public void setLeaseHistory(List<Map<String, Object>> leaseHistory) {
        this.leaseHistory = leaseHistory;
    }
}
